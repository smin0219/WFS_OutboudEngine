using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace ExpMQManager.Data
{
    public class FwbOtherChargeEntity
    {
        public FwbOtherChargeEntity()
        {
            //Empty Constructor
        }

        public FwbOtherChargeEntity(string __prepaidIndicator, string __chargeCode,
            string __entitlement, double __chargeAmt)
        {
            this.prepaidIndicator = __prepaidIndicator;
            this.chargeCode = __chargeCode;
            this.entitlement = __entitlement;
            this.chargeAmt = __chargeAmt;
        }

        private string _prepaidIndicator = "";
        public string prepaidIndicator
        {
            get { return _prepaidIndicator; }
            set { _prepaidIndicator = value; }
        }

        private string _chargeCode = "";
        public string chargeCode
        {
            get { return _chargeCode; }
            set { _chargeCode = value; }
        }

        private string _entitlement = "";
        public string entitlement
        {
            get { return _entitlement; }
            set { _entitlement = value; }
        }

        private double _chargeAmt = 0.00;
        public double chargeAmt
        {
            get { return _chargeAmt; }
            set { _chargeAmt = value; }
        }


    }
}
