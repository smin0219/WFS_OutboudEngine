using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace ExpMQManager.Data
{
    public class FwbVolumeEntity
    {
        public FwbVolumeEntity()
        {
            //Empty Constructor
        }

        public FwbVolumeEntity(double __pcsWeight, double __width, double __length,
            double __height, string __unitCode, int __pcsDim, double __volWeight)
        {
            this.pcsWeight = __pcsWeight;
            this.width = __width;
            this.length = __length;
            this.height = __height;
            this.unitCode = __unitCode;
            this.pcsDim = __pcsDim;
            this.volWeight = __volWeight;
        }
        
        private double _pcsWeight = 0.00;
        public double pcsWeight
        {
            get { return _pcsWeight; }
            set { _pcsWeight = value; }
        }

        private double _width = 0.00;
        public double width
        {
            get { return _width; }
            set { _width = value; }
        }

        private double _length = 0.00;
        public double length
        {
            get { return _length; }
            set { _length = value; }
        }

        private double _height = 0.00;
        public double height
        {
            get { return _height; }
            set { _height = value; }
        }

        private string _unitCode = "";
        public string unitCode
        {
            get { return _unitCode; }
            set { _unitCode = value; }
        }

        private int _pcsDim = 0;
        public int pcsDim
        {
            get { return _pcsDim; }
            set { _pcsDim = value; }
        }

        private double _volWeight = 0.00;
        public double volWeight
        {
            get { return _volWeight; }
            set { _volWeight = value; }
        }

    }
}
